    @staticmethod
    def get_local_ip():
        """Pick a useful local IPv4 address without assuming a fixed subnet."""
        candidates = []

        def add_candidate(ip):
            """Append one valid non-loopback IPv4 candidate once."""
            try:
                ip_obj = ipaddress.ip_address(ip)
            except ValueError:
                return
            if ip_obj.version != 4:
                return
            if ip_obj.is_loopback or ip_obj.is_link_local or ip_obj.is_multicast or ip_obj.is_unspecified:
                return
            if ip not in candidates:
                candidates.append(ip)

        for target in ("8.8.8.8", "1.1.1.1", "223.5.5.5", "114.114.114.114"):
            sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            try:
                sock.connect((target, 80))
                add_candidate(sock.getsockname()[0])
            except OSError:
                pass
            finally:
                sock.close()

        try:
            hostname = socket.gethostname()
            for info in socket.getaddrinfo(hostname, None, socket.AF_INET):
                add_candidate(info[4][0])
        except OSError:
            pass

        try:
            hostname = socket.gethostname()
            for ip in socket.gethostbyname_ex(hostname)[2]:
                add_candidate(ip)
        except OSError:
            pass

        for ip in candidates:
            if ipaddress.ip_address(ip).is_private:
                return ip
        if candidates:
            return candidates[0]
        return "127.0.0.1"

