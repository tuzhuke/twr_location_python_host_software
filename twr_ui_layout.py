    @staticmethod
    def available_screen_geometry():
        """Return primary-screen available geometry with a safe fallback."""
        screen = QtGui.QGuiApplication.primaryScreen()
        if screen is None:
            return QtCore.QRect(0, 0, 1366, 768)
        return screen.availableGeometry()

    def adaptive_px(self, value, minimum=None, maximum=None):
        """Scale a design-time pixel value by the active layout scale."""
        scale = getattr(self, "ui_scale", 1.0)
        scaled = int(round(value * scale))
        if minimum is not None:
            scaled = max(int(minimum), scaled)
        if maximum is not None:
            scaled = min(int(maximum), scaled)
        return scaled

    def configure_adaptive_window(self):
        """Choose startup/minimum window sizes from the current display."""
        available = self.available_screen_geometry()
        target_width = min(self.DESIGN_WINDOW_WIDTH, max(self.MIN_WINDOW_WIDTH, int(available.width() * 0.92)))
        target_height = min(self.DESIGN_WINDOW_HEIGHT, max(self.MIN_WINDOW_HEIGHT, int(available.height() * 0.90)))
        target_width = min(target_width, max(640, available.width() - 24))
        target_height = min(target_height, max(520, available.height() - 48))
        self.target_window_width = target_width
        self.target_window_height = target_height

        self.ui_scale = max(
            0.76,
            min(
                1.0,
                target_width / float(self.DESIGN_WINDOW_WIDTH),
                target_height / float(self.DESIGN_WINDOW_HEIGHT),
            ),
        )
        min_width = min(self.MIN_WINDOW_WIDTH, max(640, available.width() - 24))
        min_height = min(self.MIN_WINDOW_HEIGHT, max(520, available.height() - 48))
        self.resize(target_width, target_height)
        self.setMinimumSize(min_width, min_height)

